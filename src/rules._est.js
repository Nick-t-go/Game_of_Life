const _ = false;
const O = true;

test("toad phase 1 -> 2", () => {
  const start = [
    [_, _, _, _, _, _],
    [_, _, _, O, _, _],
    [_, O, _, _, O, _],
    [_, O, _, _, O, _],
    [_, _, O, _, _, _],
    [_, _, _, _, _, _],
  ];
  const end = [
    [_, _, _, _, _, _],
    [_, _, _, _, _, _],
    [_, _, O, O, O, _],
    [_, O, O, O, _, _],
    [_, _, _, _, _, _],
    [_, _, _, _, _, _],
  ];
  expect(rules(start)).toEq(end);
});

test("toad phase 2 -> 1", () => {
  const start = [
    [_, _, _, _, _, _],
    [_, _, _, _, _, _],
    [_, _, O, O, O, _],
    [_, O, O, O, _, _],
    [_, _, _, _, _, _],
    [_, _, _, _, _, _],
  ];
  const end = [
    [_, _, _, _, _, _],
    [_, _, _, O, _, _],
    [_, O, _, _, O, _],
    [_, O, _, _, O, _],
    [_, _, O, _, _, _],
    [_, _, _, _, _, _],
  ];
  expect(rules(start)).toEq(end);
});
